<h1 class="adsimple-221137402">Willkommen auf SocialNet!</h1>
<br>
<p class="adsimple-221137402">Nutzen Sie bitte Google Chrome, um unsere Website im ganzen Ausmaß genießen zu können. In der Navigationsleiste oberhalb, finden Sie die Möglichkeiten sich einzuloggen, zu registrieren, öffentliche Posts und unser Impressum anzusehen.</p>
<br>
<h2>Posts</h2>
<p class="adsimple-221137402">Öffentliche Posts sind auch für nicht-angemeldete User einsehbar, allerdings können Sie die Beiträge weder liken/disliken noch kommentieren.</p>
<br>
<h2>Profildaten</h2>
<br>
<p class="adsimple-221137402">Nachdem Sie sich registriert haben, können Sie hier Ihre angegebenen Daten einsehen und gegebenenfalls ändern. Aber vergessen Sie nicht, das Passwort muss mindestens 8 Zeichen lang sein, einen Großbuchstaben und eine Zahl enthalten. Des Weiteren wird jenes in der Datenbank gehashed, um Sie vor Hackerangriffen zu schützen.</p>
<br>
<h2>Freunde</h2>
<br>
<p class="adsimple-221137402">Eingeloggte Nutzer haben auch die Möglichkeit sich mit anderen Usern zu befreunden. Dazu müssen Sie lediglich den Nutzernamen des zukünftigen Freundes/ zukünftigen Freundin wissen und können diesen im angegebenen Feld suchen.</p>
<br>
<h2>Chat</h2>
<br>
<p class="adsimple-221137402">Befreundete User können auch miteinander chatten. Diese Nachrichten werden ausschließlich von den Teilnehmern dieser Konversation gesehen.</p>
<br>
<h2>Login</h2>
<br>
<p class="adsimple-221137402">Nachdem Sie sich auf unserer Website registriert haben, können Sie sich mit ihrem Username und Passwort einloggen, um alle Features nutzen zu können.</p>
<br>
<h2>Logout</h2>
<br>
<p class="adsimple-221137402">Wenn Sie eingeloggt sind, können Sie sich hiermit ausloggen.</p>
<br>
<h2>Impressum</h2>
<br>
<p class="adsimple-221137402">Hier finden Sie alle relevanten Informationen in Bezug auf uns.</p>